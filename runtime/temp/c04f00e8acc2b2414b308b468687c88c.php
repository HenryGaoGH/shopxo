<script>
    var isCheckAll = false;

    function swapCheck() {
        if (isCheckAll) {
            $("input[type='checkbox']").each(function() {
                this.checked = false;
            });
            isCheckAll = false;
        } else {
            $("input[type='checkbox']").each(function() {
                this.checked = true;
            });
            isCheckAll = true;
        }
    }

    function batch(){
        var number = document.getElementsByName('check');
        var arr = [];
        for(i=0;i<number.length;i++){
            if(number[i].checked){
                arr.push(number[i].value)
            }
        }
        $.ajax({
            type:'post',
            data:{id:arr},
            dataType:'json',
            url:'<?php echo Myurl('admin/supplier/batch'); ?>',
            success: function(data){
                console.log(data);
            }
        })
    }


</script>